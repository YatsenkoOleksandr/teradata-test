	CREATE TEMPORARY TABLE PRODUCT.PUBLIC.TEMP_QUAL_TABLE AS
	(
	SELECT
			CALENDARYEAR || '-' ||
			MONTHNUMBEROFYEAR AS YEARMONTH,
			CALENDARYEAR YR,
			MONTHNUMBEROFYEAR AS SALE_MONTH,
			SALESAMOUNT AS SALE_AMT
			FROM
			HR.PUBLIC.DIMDATE DD,
			SALES.PUBLIC.FACTINTERNETSALES FIS
			WHERE FIS.ORDERDATEKEY = DD.DATEKEY
	QUALIFY
			RANK() OVER (PARTITION BY YR ORDER BY SALESAMOUNT) = 1
	);

